const color = `blue`

switch (color) {
   case `red`:
      console.log(`Color is red`);
      break;
   case `blue`:
      console.log(`Color is blue`);
      break;
   case `green`:
      console.log(`Color is green`);
      break;
   case `white`:
      console.log(`Color is white`);
      break;
   default:
      console.log(`Color is not red or blue or green`);
      break;
}
